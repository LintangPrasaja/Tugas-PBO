fun main(){
    // Operator terbagi menjadi 4 macam :
    // 1. Operators Assignment
    var assignment = "Kiyomasa"

    // 2. Operators Aritmatika
    var aritmatika = 6 + 2

    // 3. Operators Comparison
    val a = 2
    var comparison = a == a

    // 4. Operators Logika
    val b = 4
    var logikaAnd = b < 2 &&  b > 5
    var logikaOr = b > 1 || b < 3

    println("1. operator Assignment :" + assignment)
    println("2. operator Aritmatika :" + aritmatika)
    println("3. operator Comparison :" + comparison)
    println("4. a.operator Logika And :" + logikaAnd)
    println("4. b.operator Logika Or :" + logikaOr)
}