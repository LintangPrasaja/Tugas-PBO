fun main() {
  // disini saya memasukan "nama" dan "kelompok" kedalam variabel
  // "val" adalah object Immutable
  // "var" adalah object Mutable
  val nama1 = "Alif Lintang"
  var kelompok1 = 4421

  println("Saya " + nama1)
  println("Saya dari kelompok " + kelompok1 + "\n")

  // atau kita dapat memasukan variabel dengan cara
  var nama2: String = "Alif Lintang"
  var kelompok2: Int = 4421

  println("Saya " + nama1)
  println("Saya dari kelompok " + kelompok1)
}