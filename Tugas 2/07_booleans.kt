fun main(){
    // Mmembuat variabel bernilai boolean
    val benar = true
    val salah = false

    println("Saya adalah seorang Mahasiswa Udinus : $benar")
    println("Saya berjenis kelamin perempuan : $salah \n")

    // Boolean biasanya digunakan sebagai operator perandingan, untuk mengetahui ekspresi tersebut
    var a = 1
    var b = 3

    print("apakah 1 lebih dari (>) 3 : ")
    println(a > b)
    print("apakah 3 sama dengan (==) 3 : ")
    println(b == 3)
}